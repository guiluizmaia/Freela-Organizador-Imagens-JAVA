    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
           
            String name = planilha.toString();
             int pos = name.lastIndexOf('.');
             String ext = name.substring(pos + 1);
             FileInputStream fileIn = null;
            try {
                fileIn = new FileInputStream(planilha);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Main1.class.getName()).log(Level.SEVERE, null, ex);
            }
             
             if (ext.equals("xlsx")) {
                try {
                    //Metodo aceita o path do arquivo
                    workbook = new XSSFWorkbook(fileIn);
                } catch (IOException ex) {
                    throw new RuntimeException(ex);
                }
             } else if (ext.equals("xls")) {
                try {
                    //Metodo nao aceita string do path do arquivo
                    workbook = new HSSFWorkbook(fileIn);
                } catch (IOException ex) {
                    throw new RuntimeException(ex);
                }
             }
             else
             {
                 throw new IllegalArgumentException("Received file does not have a standard excel extension.");
             }
        
            Sheet sheet = workbook.getSheet("Produtos");
            Row row;
            for(int j = 1; j < sheet.getLastRowNum(); j++){
                row = sheet.getRow(j);
                Cell cell0 = row.getCell(0);
                Cell cell1 = row.getCell(1);
                
                String cod = String.valueOf(cell0.getNumericCellValue());
                cod = cod.substring(0, 7);
                String url = cell1.getStringCellValue();
                
                DefaultTableModel mp = (DefaultTableModel) tb.getModel();
                mp.addRow(new String[]{cod,url});
         
            }
        
        

        
        
            
    }//GEN-LAST:event_jButton3ActionPerformed

